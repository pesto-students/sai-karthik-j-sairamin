-- To establish M-M relation, I have to create a new table

-- One Order can have multiple items.
-- One Item can be in multiple orders.
-- Therefore, this is MANY-MANY RELATION

-- There can be more than 1 ordered_quantity of an item in an order
-- So, the columns of this table would be AS FOLLOWS:
CREATE TABLE items_ordered (
	num INT PRIMARY KEY AUTO_INCREMENT,
	item_id INTEGER, -- item_id range is [1, 320]
	order_id INTEGER, -- order_id range is [1, 126]
	ordered_quantity INTEGER NOT NULL DEFAULT 1,
	FOREIGN KEY (item_id) REFERENCES items(item_no),
	FOREIGN KEY (order_id) REFERENCES orders(o_no),
	UNIQUE(item_id, order_id);
);
